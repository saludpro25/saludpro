export function PlatformSelector() {
  const [selectedPlatforms, setSelectedPlatforms] = useState<string[]>([]);
  const router = useRouter();

  const handlePlatformToggle = (platformId: string) => {
    setSelectedPlatforms(prev => 
      prev.includes(platformId)
        ? prev.filter(id => id !== platformId)
        : [...prev, platformId]
    );
  };

  const handleContinue = () => {
    if (selectedPlatforms.length > 0) {
      // Guardar plataformas seleccionadas en localStorage
      localStorage.setItem("selectedPlatforms", JSON.stringify(selectedPlatforms));
      // Redirigir a la página de agregar links
      router.push("/add-links");
    }
  };

  const handleBack = () => {
    router.back();
  };

  const handleSkip = () => {
    // Continuar sin seleccionar plataformas
    router.push("/profile-created");
  };

  return (
    <div className="min-h-screen flex flex-col items-center justify-center p-4">
      {/* Header */}
      <div className="w-full max-w-4xl mb-8">
        <div className="flex items-center justify-between mb-6">
          <button
            onClick={handleBack}
            className="flex items-center gap-2 text-gray-600 hover:text-gray-800 transition-colors"
          >
            <ArrowLeft className="w-4 h-4" />
            <span className="text-sm">Atrás</span>
          </button>
          
          <button
            onClick={handleSkip}
            className="text-gray-500 hover:text-gray-700 text-sm transition-colors"
          >
            Omitir
          </button>
        </div>

        {/* Title */}
        <div className="text-center mb-8">
          <h1 className="text-3xl font-bold text-gray-900 mb-2">
            ¿En qué plataformas te encuentras?
          </h1>
          <p className="text-gray-600">
            Selecciona hasta cinco para comenzar. Puedes actualizar en cualquier momento.
          </p>
        </div>
      </div>

      {/* Platforms Grid */}
      <div className="w-full max-w-4xl mb-8">
        <div className="grid grid-cols-3 gap-4 md:gap-6">
          {platforms.map((platform) => {
            const isSelected = selectedPlatforms.includes(platform.id);
            return (
              <button
                key={platform.id}
                onClick={() => handlePlatformToggle(platform.id)}
                className={`relative group p-4 rounded-2xl border-2 transition-all duration-300 ${
                  isSelected
                    ? "border-purple-500 bg-purple-50 scale-105"
                    : "border-gray-200 hover:border-gray-300 hover:scale-102"
                }`}
                disabled={!isSelected && selectedPlatforms.length >= 5}
              >
                {/* Platform Icon */}
                <div className={`w-16 h-16 rounded-xl ${platform.bgColor} flex items-center justify-center mb-3 mx-auto`}>
                  {platform.icon}
                </div>

                {/* Platform Name */}
                <p className="text-sm font-medium text-gray-900 text-center">
                  {platform.name}
                </p>

                {/* Selection Indicator */}
                {isSelected && (
                  <div className="absolute -top-2 -right-2 w-6 h-6 bg-purple-500 rounded-full flex items-center justify-center">
                    <Check className="w-4 h-4 text-white" />
                  </div>
                )}

                {/* Disabled Overlay */}
                {!isSelected && selectedPlatforms.length >= 5 && (
                  <div className="absolute inset-0 bg-gray-100/80 rounded-2xl flex items-center justify-center">
                    <span className="text-xs text-gray-500 font-medium">Máximo 5</span>
                  </div>
                )}
              </button>
            );
          })}
        </div>
      </div>

      {/* Selected Count */}
      {selectedPlatforms.length > 0 && (
        <div className="mb-6">
          <p className="text-sm text-gray-600 text-center">
            {selectedPlatforms.length} de 5 plataformas seleccionadas
          </p>
        </div>
      )}

      {/* Continue Button */}
      <div className="w-full max-w-md">
        <button
          onClick={handleContinue}
          disabled={selectedPlatforms.length === 0}
          className={`w-full py-3 px-6 rounded-full font-medium transition-all duration-300 flex items-center justify-center gap-2 ${
            selectedPlatforms.length > 0
              ? "bg-purple-600 text-white hover:bg-purple-700 shadow-lg hover:shadow-xl"
              : "bg-gray-200 text-gray-400 cursor-not-allowed"
          }`}
        >
          <span>Continuar</span>
          <ArrowRight className="w-4 h-4" />
        </button>
      </div>
    </div>
  );
}